        {"역사적 사건": "2020년 코로나19 팬데믹 (3월)", "공포의 정점 이격도": "74% ~ 79%", "당시 시장 심리 상태": "인류 마비 공포, 코스피 서킷브레이커 발동", "이격도 정점 이후 실제 결과": "역대 최악의 이격도 과매도 기록 후, 4월 한 달 만에 20일선 안착 및 역사적 대세 상승장 시발점 돌입"},
